    public void InvoiceLoad() {
        try {
            DefaultTableModel dt = (DefaultTableModel) sales_table.getModel();
            dt.setRowCount(0);

            Statement s = SQLConnection.getConnection().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM cart");

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));
                v.add(rs.getString(5));
                v.add(rs.getString(6));
                v.add(rs.getString(7));

                dt.addRow(v);
            }

        } catch (Exception e) {
            System.out.println(e);
        }

        //load customer start
        try {

            Statement s = SQLConnection.getConnection().createStatement();
            ResultSet rs = s.executeQuery("SELECT DISTINCT `Product_Name` FROM cart;");
            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString("Product_Name"));
                DefaultComboBoxModel com = new DefaultComboBoxModel(v);
                ProdNameCB.setModel(com);
                //CustomersCB.setSelectedItem(null);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        //load customer end
    }

